# Profundizamos en los sistemas de numeracion

# SIstema decimal
a = 10
print(f"a: {a}")
# Sistema binario
a = 0b1010
print(f"a: {a}")
# Sistema octal
a = 0o12
print(f"a: {a}")
# Sistema hexadecimal
a = 0xA
print(f"a: {a}")

# Base decimal
a = int('23', 10)
print(f"a: {a}")
# Base binario
a = int('1010', 2)
print(f"a: {a}")

# Base octal
a = int('12', 8)
print(f"a: {a}")

# Base hexadecial
a = int('A', 16)
print(f"a: {a}")

# Base 5 sus valos son de 0 a 4
a = int('344', 5)
print(f"a: {a}")